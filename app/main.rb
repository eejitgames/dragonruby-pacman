  def draw_background
    outputs.background_color = [ 9, 9, 9 ]
  end

  def draw_sprite x, y, n
    size = 16
    outputs.sprites << { x: (x + 26) * size, y: (y + 6) * size, w: size, h: size, path: "sprites/maze-parts/#{n}.png" }
  end

  def draw_maze
    31.times do |y|
      28.times do |x|
        draw_sprite x, y, @maze[y][x]
      end
    end      
  end

  def tick
    defaults
    draw_background
    draw_maze
  end
